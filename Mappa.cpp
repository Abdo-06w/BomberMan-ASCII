bool Mappa::endGame() {

    if (getTimer()->getTime() <= 0 || current->game->getPlayer()->getVita() <= 0) {
        napms(700);
        won = false;
        return true;
    }

    bool noNemici = current->game->getNumNemici() <= 0;
    bool noPorta = !current->livello->isTherePortaNext() && !current->livello->isTherePortaPrev();
    if (noNemici && noPorta) {
        current->game->update(this);
        napms(700);
        won = true;
        return true;
    }

    return false;
}

