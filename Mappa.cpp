Mappa::~Mappa() {

    delete points;
    points = NULL;

    delete time;
    time = NULL;

    while (current && current->prev != NULL)current = current->prev;

    while (current) {
        Maps* tmp = current->next;
        delete current;
        current = tmp;
    }
    current = NULL;

}
