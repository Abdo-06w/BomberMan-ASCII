void Mappa::update(Player* p) {



    timer();

    Position pos = p->getPosition();

    if (current->game->getNumNemici() <= 0) {
        delRoom();

        return;
    }

    if (current->livello->isPortaNext(pos.y,pos.x)){
        nextRoom();
        return;
    }
    if (current->livello->isPortaPrev(pos.y,pos.x)) {
        prevRoom();
        return;
    }

}



